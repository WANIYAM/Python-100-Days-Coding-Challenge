# 40.Write a program to count occurrences of an element in a tuple.

# Define the tuple
my_tuple = (1, 3, 7, 8, 7, 5, 6, 7, 2, 1)

# Element to count
element = 7

# Count occurrences using the count() method
count = my_tuple.count(element)

# Display the result
print(f"The element {element} appears {count} times in the tuple.")
