# 72.Use built-in math module (factorial, sqrt, gcd).
import math

# Using factorial()
num = 5
fact = math.factorial(num)
print(f"Factorial of {num} is {fact}")

# Using sqrt()
value = 16
square_root = math.sqrt(value)
print(f"Square root of {value} is {square_root}")

# Using gcd()
a = 36
b = 60
common_divisor = math.gcd(a, b)
print(f"GCD of {a} and {b} is {common_divisor}")
