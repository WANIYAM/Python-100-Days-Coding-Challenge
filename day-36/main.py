# 36.Write a program to merge two sets.

# Define two sets
set1 = {1, 2, 3, 4}
set2 = {3, 4, 5, 6}

# Merge the sets using union
merged_set = set1.union(set2)

# Alternatively, you can use the '|' operator
# merged_set = set1 | set2

# Display the result
print("Set 1:", set1)
print("Set 2:", set2)
print("Merged Set:", merged_set)
